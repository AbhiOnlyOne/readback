module TraineesHelper
  def circled_number(num)
    case num
    when 1 then "①"
    when 2 then "②"
    when 3 then "③"
    when 4 then "④"
    when 5 then "⑤"
    when 6 then "⑥"
    when 7 then "⑦"
    when 8 then "⑧"
    when 9 then "⑨"
    when 10 then "⑩"
    when 11 then "⑪"
    when 12 then "⑫"
    when 13 then "⑬"
    when 14 then "⑭"
    when 15 then "⑮"
    when 16 then "⑯"
    when 17 then "⑰"
    when 18 then "⑱"
    when 19 then "⑲"
    when 20 then "⑳"
    end
  end
end
